-- Attach client-side hero effects for a reconnecting player
function CourierRunGameMode:OnPlayerReconnected( event )
	local nReconnectedPlayerID = event.PlayerID
	for _, hero in pairs( Entities:FindAllByClassname( "npc_dota_hero" ) ) do
		if hero:IsRealHero() then
			self:_SpawnHeroClientEffects( hero, nReconnectedPlayerID )
		end
	end
end

function CourierRunGameMode:MainThink()
	if GameRules:State_Get() > DOTA_GAMERULES_STATE_HERO_SELECTION then
		for i = 0, (DOTA_MAX_TEAM_PLAYERS - 1) do
			local player = PlayerResource:GetPlayer(i)
			if player then
				local hero = player:GetAssignedHero()
				if hero and hero.already_deal~=true then
          local hero_position=player:GetAssignedHero():GetAbsOrigin()				
          hero:Stop()           
          hero:SetControllableByPlayer(-1, true)
          hero:SetAbilityPoints(0)
          hero:AddAbility("ability_dummy")
          hero:FindAbilityByName("ability_dummy"):SetLevel(1)
          hero:SetDayTimeVisionRange(0)
          hero:SetNightTimeVisionRange(0)
          hero.already_deal=true
          FireGameEvent("show_gamerules_popup", { player_ID = i })
          player:GetAssignedHero():SetAbsOrigin(hero_position- Vector(0,0,1000)) --英雄埋到地下
          local rand_number= RandomInt(1,#CourierRunGameMode._courierList)
          local new_courier_name=CourierRunGameMode._courierList[rand_number]
          local slot_number=CourierRunGameMode._courierSlotNumberList[new_courier_name]
          PrecacheUnitByNameAsync(new_courier_name,function() end)
          local player_new_courier = SpawnUnitForPlayer(new_courier_name, player, 
          player:GetAssignedHero():GetAbsOrigin()+Vector(0,0,1000), slot_number, true, false, 80, true)
          player_new_courier.rated_load=tonumber(CourierRunGameMode._courierRatedLoadList[new_courier_name])
          player_new_courier:CreatureLevelUp(player_new_courier.rated_load-1)
          local particle= ParticleManager:CreateParticle("particles/neutral_fx/roshan_spawn.vpcf",PATTACH_ABSORIGIN_FOLLOW,player_new_courier)
				end
			end
		end
      if self.change_audience_model_count<=0 then
      self:Change_all_audience_model()
      self.change_audience_model_count=200
      end
      self.change_audience_model_count=self.change_audience_model_count-1
	end	
	if GameRules:State_Get() >= DOTA_GAMERULES_STATE_GAME_IN_PROGRESS then
		self:Stateboard()
	end
	return 1
end


function CourierRunGameMode:Change_all_audience_model()
     local audiences=Entities:FindAllByName("npc_courier_audience")
     print("audiences_number"..#audiences)
     for _,audience in pairs(audiences) do
       local new_model = courier_table[RandomInt(1,100)]
       audience:SetModel(new_model)
       audience:SetOriginalModel(new_model)
    end
end


function CourierRunGameMode:OnEntityKilled( event )
  local killedUnit = EntIndexToHScript( event.entindex_killed )
    if killedUnit then
     if killedUnit:IsHero() then
        if killedUnit:GetTeam()==DOTA_TEAM_GOODGUYS then
           GameRules:MakeTeamLose( DOTA_TEAM_GOODGUYS )
        elseif killedUnit:GetTeam()==DOTA_TEAM_BADGUYS then
          GameRules:MakeTeamLose( DOTA_TEAM_BADGUYS )
        end
     end
     local player_id=killedUnit:GetContext("owner_id")
     if killedUnit:GetContext("owner_id") and killedUnit.die_in_peace~=true then
       local player_id=killedUnit:GetContext("owner_id")
       local player=PlayerResource:GetPlayer(player_id)
       local rand_number= RandomInt(1,#CourierRunGameMode._courierList)
       local new_courier_name=CourierRunGameMode._courierList[rand_number]
       local slot_number=CourierRunGameMode._courierSlotNumberList[new_courier_name]
       PrecacheUnitByNameAsync(new_courier_name,function() end)
       local wp_reborn = Entities:FindByName( nil, "wp_reborn" )
       local player_new_courier = SpawnUnitForPlayer(new_courier_name, player, 
       wp_reborn:GetAbsOrigin(),slot_number, true, false, 80, true)
       player_new_courier.rated_load=tonumber(CourierRunGameMode._courierRatedLoadList[new_courier_name])
       player_new_courier:CreatureLevelUp(player_new_courier.rated_load-1)
       local particle= ParticleManager:CreateParticle("particles/neutral_fx/roshan_spawn.vpcf",PATTACH_ABSORIGIN_FOLLOW,player_new_courier)
    end
  end
end





