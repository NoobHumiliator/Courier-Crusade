function CourierRunGameMode:new(o)
	o = o or {}
	setmetatable(o, CourierRunGameMode)
	return o
end

function CourierRunGameMode:InitGameMode()
	CourierRunGameMode = self
  Timers:start()
  AMHCInit()
	GameRules:SetHeroSelectionTime( 45.0 )
	GameRules:SetPreGameTime( 5.0 )
	GameRules:SetPostGameTime( 6.0 )
	self._good_hero_handle = nil
	self._bad_hero_handle = nil
  self.change_audience_model_count=0   --每一分钟为观众换一次模型   
	-- Hook into game events allowing reload of functions at run time
	ListenToGameEvent( "player_reconnected", Dynamic_Wrap( CourierRunGameMode, 'OnPlayerReconnected' ), self )
	ListenToGameEvent( "entity_killed", Dynamic_Wrap( CourierRunGameMode, 'OnEntityKilled' ), self )
	ListenToGameEvent( "game_rules_state_change", Dynamic_Wrap( CourierRunGameMode, "OnGameRulesStateChange" ), self )
  --ListenToGameEvent( "npc_spawned", Dynamic_Wrap( CourierRunGameMode, "OnNPCSpawned" ), self )
	self._PlayerInitialized={}
	for i = 0, (DOTA_MAX_TEAM_PLAYERS - 1) do
	  self._PlayerInitialized[i]=false    --游戏开始的时候，初始化数组全部清零			 
	end	
	Timers:CreateTimer(
		function()
			return self:MainThink()
		end
	)
end






function CourierRunGameMode:CourierListRefill()
  local courierListKV = LoadKeyValues("scripts/npc/npc_couriers_precache.txt")
  if self._courierList==nil then
  	 self._courierList={}
  end
  if self._courierFlyModelList==nil then   --飞行模型的储存位置
  	 self._courierFlyModelList={}
  end
  if self._courierSlotNumberList==nil then  --格子数的数组
  	 self._courierSlotNumberList={}
  end
  if self._courierRatedLoadList==nil then   --额定负载
  	 self._courierRatedLoadList={}
  end
  for k, v in pairs( courierListKV ) do--for k, v in pairs( kv ) do
    if type( v ) == "table" then  --包含信使信息的table
    table.insert(self._courierList, k)
    self._courierFlyModelList[k] = v.FlyModel
    self._courierSlotNumberList[k] = tonumber(v.SlotNumber)
    self._courierRatedLoadList[k] = v.RatedLoad
    print("precached courier is "..k)
    print("his model is"..self._courierFlyModelList[k])
    print(#self._courierList)
    end
  end
end


